    private void createAccount() {
        String username = usernameField.getText();
        String password = passwordField.getText();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and Password cannot be empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection con = DatabaseConnection.getConnection();
        
        if (con != null) {
            try {
                String checkUser = "SELECT * FROM created_account WHERE username = ?";
                PreparedStatement checkPst = con.prepareStatement(checkUser);
                checkPst.setString(1, username);
                ResultSet rs = checkPst.executeQuery();
                
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Username already exists! Choose another.", "Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                String sql1 = "INSERT INTO login_user (username, password) VALUES (?, ?)";
                String sql2 = "INSERT INTO created_account (username, password) VALUES (?, ?)";
                PreparedStatement pst1 = con.prepareStatement(sql1);
                PreparedStatement pst2 = con.prepareStatement(sql2);
                pst1.setString(1, username);
                pst1.setString(2, password);
                  
                
                pst2.setString(1, username);
                pst2.setString(2, password);
                
                int rows1 = pst1.executeUpdate();
                int rows2 = pst2.executeUpdate();
                
                if (rows1 > 0) {
                    JOptionPane.showMessageDialog(this, "Account Created Successfully!");
                    
                } else if (rows2 > 0) {
                JOptionPane.showMessageDialog(this, "Account Created Successfully!");
                }
                
                else {
                    JOptionPane.showMessageDialog(this, "Account Creation Failed!", "Error", JOptionPane.ERROR_MESSAGE);
                }
                
                rs.close();
                checkPst.close();
                pst1.close();
                pst2.close();
                con.close();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Database Connection Failed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

